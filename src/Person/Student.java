package Person;
public class Student {
    private int Student_Id;
    private String name;
    //getters, setters for Student_Id
    public int getId() {
        return Student_Id;
    }
    public void setId(int s_id) {
        this.Student_Id = s_id;
    }

    //getters,setters for Student names.
    public String getName() {
        return name;
    }
    public void setName(String s_name) {
        this.name = s_name;
    }
}
